package Calc;

public class Calc {
	public long add(int n1, int n2) {
		return n1+n2;
	}
	
	public long sub(int n1, int n2) {
		return n1-n2;
	}
	
	public long mul(int n1, int n2) {		
		return n1*n2;
	}
	
	/**
	 * 
	 * @param n1
	 * @param n2
	 * @return
	 * @throws ArithmeticException
	 */
	
	public long div(int n1, int n2) {
		return n1/n2;
	}
}
